'''
윤년은 4로 나누어지는 해 중에서 100으로 나누어지지 않거나 400으로 나누어지는 해를 말합니다. 
사용자로부터 입력을 받아 해당 년도가 윤년인지 아닌지 판단하여,
윤년이면 "True", 그렇지 않으면 "False"를 출력하세요.
'''

# 사용자로부터 연도를 입력받습니다.
year = int(input("연도를 입력하세요: "))

# 4로 나누어지는지 확인합니다.
if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
    print("True")
else:
    print("False")  # 4로 나누어지지 않으면 윤년 아님
