# (첫 숫자와 마지막 숫자를 포함합니다. 첫 숫자는 두번째 숫자보다 작습니다.)

scores = [70, 81, 65, 67, 65, 95, 97, 78, 82, 63, 73, 56, 77, 68, 74, 98, 84, 17, 45, 82, 91, 57, 77, 62, 65, 51, 83, 67, 69, 49, 55, 43, 83, 70, 90, 100, 55, 37, 40, 97, 71, 74, 74, 100, 95, 96, 60, 93, 87, 59, 72, 100, 86, 65, 57, 75, 46, 98, 75, 48, 75, 77, 68, 75, 54, 59, 58, 64, 61, 49, 27, 74, 82, 65, 78, 82, 93, 62, 71, 65, 94, 52, 61, 100, 100, 86, 62, 75, 100, 67, 100, 43, 46, 33, 87, 38, 56, 44, 79, 77]

def get_average(scores, start, end):
    """
    scores: list of int
    start: int, 시작 인덱스 (0부터)
    end:   int, 끝 인덱스 (포함)
    return: float, 해당 구간 평균
    """
    # 슬라이싱: end 인덱스까지 포함하려면 end+1
    subset = scores[start:end+1]
    return sum(subset) / len(subset)


# 사용자 입력 (0부터 시작하는 인덱스라고 가정)
start = int(input())
end   = int(input())

result = get_average(scores, start, end)
print("{:.2f}".format(result))